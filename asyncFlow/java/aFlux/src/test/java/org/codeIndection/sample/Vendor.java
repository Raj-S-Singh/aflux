package org.codeIndection.sample;

public class Vendor {


	String city;
	Float sales;

	
	
	
	public Vendor(String city, Float sales) {
		super();
		this.city = city;
		this.sales = sales;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public Float getSales() {
		return sales;
	}
	public void setSales(Float sales) {
		this.sales = sales;
	}
	
	
	
	
}
