package de.tum.in.aflux.component.files.model;

public class FileToolData {
	
	String name;
	String content;
	
	
	
	public FileToolData() {
		super();
		// TODO Auto-generated constructor stub
	}
	public FileToolData(String name, String content) {
		super();
		this.name = name;
		this.content = content;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	@Override
	public String toString() {
		return String.format("FileToolData [name=%s, content=%s]", name, content);
	}

	
	
}
